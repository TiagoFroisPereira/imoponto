export const getAllConcelhos = (): { name: string; distrito: string; lat: number; lng: number }[] => {
  const allConcelhos: { name: string; distrito: string; lat: number; lng: number }[] = [];
  
  distritos.forEach((distrito) => {
    distrito.concelhos.forEach((concelho) => {
      allConcelhos.push({
        name: concelho.name,
        distrito: distrito.name,
        lat: concelho.lat,
        lng: concelho.lng,
      });
    });
  });
  
  return allConcelhos.sort((a, b) => a.name.localeCompare(b.name));
};

export const getDistritoNames = (): string[] => {
  return distritos.map((d) => d.name).sort();
};

export const getConcelhosForDistrito = (distritoName: string): Concelho[] => {
  const distrito = distritos.find((d) => d.name === distritoName);
  return distrito ? distrito.concelhos.sort((a, b) => a.name.localeCompare(b.name)) : [];
};
